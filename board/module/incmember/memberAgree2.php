⑥ 다른 이용자 또는 제3자의 저작권등 기타 권리를 침해하는 행위
⑦ 기타 관계 법령에 위배되는 행위
2. 이용자는 이 약관에서 규정하는 사항과 서비스 이용안내 또는 주의사항을 준수하여야 합니다.
3. 이용자가 설치하는 단말기 등은 전기통신설비의 기술기준에 관한 규칙이 정하는 기준에 적합하여야 하며, 서비스에 장애를 주지 않아야 합니다.


제 4 장 서비스 이용 요금 

제 18 조 (이용요금)
서비스 이용료는 무료로 합니다. 단, NIMS의 정책에 따라 이용 요금이 유료화 될 경우에는 온라인으로 서비스 화면에 게시합니다. 


제 5 장 저작권 

제 19 조 (게재된 자료에 대한 권리)
서비스에 게재된 자료에 대한 권리는 다음 각 호와 같습니다. 
1. 게시물에 대한 권리와 책임은 게시자에게 있으며, NIMS는 게시자의 동의 없이는 이를 영리적 목적으로 사용할 수 없습니다. 단, 비영리적 목적인 경우, 게시자의 동의 없이도 이를 사용할 수 있으며 서비스내의 게재권을 갖습니다
2. 게시자의 사전 동의가 없이는 이용자는 서비스를 이용하여 얻은 정보를 가공, 판매하는 행위 등 서비스에 게재된 자료를 상업적 목적으로 이용할 수 없습니다. 


제 6 장 이의 신청 및 손해배상 청구 금지 

제 20 조 (이의신청금지)
